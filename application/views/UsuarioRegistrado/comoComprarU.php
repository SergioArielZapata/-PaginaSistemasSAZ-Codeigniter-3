    <br>
    <br>
        <p>La tienda web de Sistemas SAZ InforPOWER existe para agilizar la experiencia de compra del usuario y facilitar la misma para personas del interior del país. Es por 
        eso que los precios de la pagina web, son exclusivos de la pagina web y pueden variar respecto a otros medios de venta.
        </p>
        <p>Ademas contamos con un mail de atencion exclusivo para las compras por este medio que lo encontras en el header de la tienda o en la sección contactos tenes 
        un formulario del mismo. Esto agilizara aun mas tu compra en caso de necesitar contactarte con nosotros.
        </p>
        <p>Para comprar en esta tienda seguí los pasos:</p>
        <ol>
            <li><p>Elige el producto que deseas comprar.</p></li>
            <li><p>Haz clic en el botón de "Agregar al carrito". Esto agregará el producto a tu carrito y te llevará al mismo.</p>
            <ul>
                <li><p>En la pagina de cada producto podes cotizar el envio por codigo postal, SOLO DE ESE PRODUCTO.</p></li>
            </ul>
            </li>
            <li><p>Puedes seguir agregando otros productos al carrito o sino haz clic en "Iniciar Compra".</p> 
                <ul><li><p>Luego de iniciar la compra, podes calcular el envio de TODO EL CARRITO.</p></li></ul>
            </li>
            <li><p>Completa tus datos de contacto y haz clic en "Continuar".</p>
                <ul><li><p>SI necesitas un tipo de facturacion especial ingresa el cuit en el campo especificado, nosotros lo facturamos acorde los datos de la afip. </p></li></ul>
            </li>
            <li><p>Ingresa la dirección a donde deseas recibir el producto. Luego haz clic en "Continuar".</p></li>
            <li><p>Selecciona el método de envío que desees y haz clic en "Continuar".</p>
                <ul>
                    <li><p>Asegurate de escribir bien tu DNI, dado que luego no podras retirar en ninguna sucursal de medio de envio que elegiste.</p></li>
                    <li><p>Los envíos los realizamos a través de los medios publicados cuando terminas la compra.</p></li>
                </ul>
            </li>
            <li><p>Elige el medio de pago.</p>
                <ul>
                    <li><p>Tarjeta de credito o debito (procesado por MercadoPago o Todopago)</p></li>
                    <li><p>También puedes seleccionar la opción de "Transferencia/Deposito Bancario".</p></li>
                    <li><p>Efectivo con envio en puntos de pagofacil o rapipago.</p></li>
                    <li><p>Efectivo "Transferencia/Deposito Bancario" sin envio, podes abonarlo en el local en el momento de retirarlo.</p></li>
                    <li><p>Una vez que hayas elegido el medio de pago, haz clic en "Continuar".</p></li>
                </ul>
            </li>
            <li><p>Finalmente en la página de Confirmación de compra puedes revisar toda la información de la compra. Asegurate que todo este correcto. Luego haz clic en "Continuar".</p></li>
            <li><p>Ahí serás redirigido a otra pantalla para que completes los datos sobre la forma de pago elegida. Después de confirmar la compra recibirás un correo de nuestra parte, 
                ese no será un comprobante de pago, el mismo sera enviado por mail mas tarde</p>
            </li>
            <li><p>10-Una vez acreditado el pago, haremos el envío correspondiente de los productos que hayas comprado y la factura electrónica se enviara por el mail con el que te
                registraste.</p>
        </ol>
        <p class="font-weight-bolder">Si requerís facturación especial: A, IVA exento, asegurate de completarlo en el campo opcional en el checkout o si te olvidaste envíanos un mail a penas terminás la 
            compra con asunto FACTURA X y el CUIT y tipo de factura con el número de compra o mail (con el que hiciste la compra) en el cuerpo del mismo.</p>     
